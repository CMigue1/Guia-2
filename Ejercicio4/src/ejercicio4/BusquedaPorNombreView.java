    private void jtPorNombreKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtPorNombreKeyReleased
        // TODO add your handling code here:
        borrarFilas();
        for(Producto prod : Menu.listaProductos){
            if(prod.getDescripcion().startsWith(jtPorNombre.getText())){
                modelo.addRow(new Object[]{
                    prod.getCodigo(),
                    prod.getDescripcion(),
                    prod.getPrecio(),
                    prod.getStock()
                });
            }
        }
    }//GEN-LAST:event_jtPorNombreKeyReleased


